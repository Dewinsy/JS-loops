console.log("while loop");
var logs = 0;
while (logs < 10) {
  
  logs++;
  console.log(logs);
}
console.log("****************************************************");

console.log("for loop");
for (var nums = 0; nums <= 10; nums++) {
  
  console.log(nums);
}
console.log("****************************************************");

console.log("do-while loops");
let trials = 0;
do{
    trials++;
    console.log(trials);
}
while(trials<=9);
console.log("****************************************************");


